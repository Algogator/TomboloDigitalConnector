package uk.org.tombolo.reader.spec;

public class SeriesInstance {

	String label;
	int dataColumnId;
	int startLine;
	int endLine;
	
	public String getLabel() {
		return label;
	}
	public int getDataColumnId() {
		return dataColumnId;
	}
	public int getStartLine() {
		return startLine;
	}
	public int getEndLine() {
		return endLine;
	}
}
